import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const frontendRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const contractsRoot = resolve(
  process.env.AMMO_CONTRACTS_DIR ?? resolve(frontendRoot, "../ammo.wtf-contracts")
);

const contracts = [
  {
    artifact: "out/AmmoFactory.sol/AmmoFactory.json",
    exportName: "ammoFactoryAbi",
    output: "src/abi/ammoFactory.ts",
  },
  {
    artifact: "out/AmmoFactory.sol/AmmoToken.json",
    exportName: "ammoTokenAbi",
    output: "src/abi/ammoToken.ts",
  },
  {
    artifact: "out/AmmoBatchRedeemer.sol/AmmoBatchRedeemer.json",
    exportName: "ammoBatchRedeemerAbi",
    output: "src/abi/ammoBatchRedeemer.ts",
  },
];

execFileSync("forge", ["build", "--root", contractsRoot], {
  stdio: "inherit",
});

for (const contract of contracts) {
  const artifactPath = resolve(contractsRoot, contract.artifact);
  const artifact = JSON.parse(readFileSync(artifactPath, "utf8"));

  if (!Array.isArray(artifact.abi)) {
    throw new Error(`Artifact has no ABI array: ${artifactPath}`);
  }

  const source = `// Generated by yarn generate:abis. Do not edit manually.
export const ${contract.exportName} = ${JSON.stringify(artifact.abi, null, 2)} as const;

export default ${contract.exportName};
`;

  writeFileSync(resolve(frontendRoot, contract.output), source);
  console.log(`Generated ${contract.output}`);
}
